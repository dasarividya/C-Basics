/* Calculation Of Area And Perimeter */
#include<stdio.h>
void main()
{
	// Variable Declaration
	int n, a, p;
	
	// Initialization
	n= 4;
	
	// Calculation
	a= n*n;
	p= 4*n;
	
	// Output
	printf(" Area of the square with side %d is: %d\n", n, a);
	printf(" Perimeter of the square with side %d is : %d", n, p);
	
	
}
