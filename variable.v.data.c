/* Print data of the student */
#include<stdio.h>
void main()
{
	// Variable Declaration
	int age;
	float cgpa;
	char initial;
	
	// Initialization
	age = 19;
	cgpa = 8.5;
	initial = 'D';
	
	// Output
	printf("student age is: %i\n", age);
	printf("cgpa of the student is: %f\n", cgpa);
	printf("initial of the student is : %c\n", initial);
}
